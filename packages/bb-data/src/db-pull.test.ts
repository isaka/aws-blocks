// ── Supabase Auth detection regex ──────────────────────────────────────

describe('Supabase Auth detection', () => {
  // This is the regex used in introspect() to identify incompatible policies
  const isSupabaseAuthPolicy = (text: string) => /\bauth\.(uid|email|role)\s*\(/.test(text);

  test('detects auth.uid()', () => {
    assert.ok(isSupabaseAuthPolicy('(id = auth.uid())'));
    assert.ok(isSupabaseAuthPolicy('(( SELECT auth.uid() AS uid) = user_id)'));
  });

  test('detects auth.email() and auth.role()', () => {
    assert.ok(isSupabaseAuthPolicy("(email = auth.email())"));
    assert.ok(isSupabaseAuthPolicy("(role = auth.role())"));
  });

  test('does NOT flag auth.jwt() — it is OIDC-compatible', () => {
    assert.ok(!isSupabaseAuthPolicy("(user_id = (auth.jwt() ->> 'sub'::text))"));
    assert.ok(!isSupabaseAuthPolicy("((auth.jwt() -> 'app_metadata'::text) ->> 'role')"));
  });

  test('does NOT flag current_setting-based policies', () => {
    assert.ok(!isSupabaseAuthPolicy("(user_id = (current_setting('request.jwt.claims', true)::jsonb ->> 'sub'))"));
  });
});

