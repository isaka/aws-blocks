describe('selectEligibleTables', () => {
  const noPkTable: TableInfo = {
    name: 'audit_log',
    columns: [{ table_name: 'audit_log', column_name: 'msg', data_type: 'text', is_nullable: 'YES', column_default: null, ordinal_position: 1 }],
    primaryKey: [],
    autoGenerated: [],
    hasRls: false,
    missingGrants: false,
  };

  function intro(tables: TableInfo[], supabaseAuth: string[] = []): IntrospectionResult {
    return {
      tables,
      tablesUsingSupabaseAuth: new Set(supabaseAuth),
      nonStandardClaims: new Map(),
    };
  }

  test('keeps tables with a primary key that do not use Supabase Auth', () => {
    const result = selectEligibleTables(intro(TABLES));
    assert.deepStrictEqual(result.map(t => t.name), ['migration_test_todos', 'categories']);
  });

  test('excludes tables with no primary key', () => {
    const result = selectEligibleTables(intro([...TABLES, noPkTable]));
    assert.ok(!result.some(t => t.name === 'audit_log'));
  });

  test('excludes tables that use Supabase Auth (auth.uid())', () => {
    const result = selectEligibleTables(intro(TABLES, ['migration_test_todos']));
    assert.deepStrictEqual(result.map(t => t.name), ['categories']);
  });
});

// ── writeTypesAndMeta (dev-loop type refresh primitive) ─────────────────

describe('writeTypesAndMeta', () => {
  function tmpDir(): string {
    return fs.mkdtempSync(path.join(os.tmpdir(), 'bb-data-regen-'));
  }

  test('writes ONLY database.types.ts + database.meta.ts and nothing else', () => {
    const dir = tmpDir();
    writeTypesAndMeta(dir, TABLES);
    const written = fs.readdirSync(dir).sort();
    assert.deepStrictEqual(written, ['database.meta.ts', 'database.types.ts']);
    // Must not produce any of the one-time `db pull` scaffolding.
    for (const f of ['.env.local', '.env.production.example', 'supabase.ts', 'MIGRATION_GUIDE.md', 'index.ts']) {
      assert.ok(!fs.existsSync(path.join(dir, f)), `should not write ${f}`);
    }
    assert.ok(!fs.existsSync(path.join(path.dirname(dir), 'migrations', '000_baseline.sql')), 'must not touch the baseline');
  });

  test('content matches the canonical generators', () => {
    const dir = tmpDir();
    writeTypesAndMeta(dir, TABLES);
    assert.strictEqual(fs.readFileSync(path.join(dir, 'database.types.ts'), 'utf-8'), generateTypesFile(TABLES));
    assert.strictEqual(fs.readFileSync(path.join(dir, 'database.meta.ts'), 'utf-8'), generateMetaFile(TABLES));
  });

  test('preserves hand-edited singulars passed in', () => {
    const dir = tmpDir();
    const handEdited = new Map([['categories', 'cat']]);
    writeTypesAndMeta(dir, TABLES, handEdited);
    const meta = fs.readFileSync(path.join(dir, 'database.meta.ts'), 'utf-8');
    assert.ok(meta.includes("singular: 'cat'"), 'uses the hand-edited singular');
    // Round-trips through the parser that the dev loop uses to read it back.
    assert.strictEqual(parseExistingMetaSingulars(meta).get('categories'), 'cat');
  });

  test('re-running into the same dir overwrites without leaving extra files', () => {
    const dir = tmpDir();
    writeTypesAndMeta(dir, TABLES);
    writeTypesAndMeta(dir, [TABLES[0]]);
    assert.deepStrictEqual(fs.readdirSync(dir).sort(), ['database.meta.ts', 'database.types.ts']);
  });
});
