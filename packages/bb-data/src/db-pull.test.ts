describe('generateIndexFile', () => {
  const output = generateIndexFile(TABLES, { projectRef: 'abcdef', runtimeConnString: 'postgresql://x:y@host:6543/postgres' });

  test('generated CRUD does not import a bundled auth Building Block', () => {
    // supabaseCrud receives auth via an optional parameter, so the generated
    // index stays auth-agnostic and must not import any @aws-blocks/bb-auth* block.
    assert.ok(!output.includes('@aws-blocks/bb-auth'));
  });

  test('supabaseCrud accepts optional auth parameter', () => {
    assert.ok(output.includes('supabaseCrud(context: any, auth?:'));
  });

  test('throws helpful error when auth not configured', () => {
    assert.ok(output.includes("Auth not configured"));
  });

  test('includes stage-scoped AppSetting name (ref-independent)', () => {
    assert.ok(output.includes("dbConnectionParameterName(process.env.BLOCKS_STAGE ?? 'sandbox')"));
    assert.ok(output.includes("from '@aws-blocks/core/db-naming'"));
  });

  test('resolves SSM parameter name from blocks-config at runtime (not BLOCKS_STAGE)', () => {
    // At Lambda runtime BLOCKS_STAGE is unset; the name must come from the
    // deploy-stamped BLOCKS_SSM_PARAM_DB_URL or the prod Lambda requests the
    // sandbox parameter and gets AccessDenied.
    assert.ok(output.includes('process.env.BLOCKS_SSM_PARAM_DB_URL'));
    // The deploy-time fallback (?? ...) still computes the stage-scoped name.
    assert.ok(output.includes('?? dbConnectionParameterName('));
  });

  test('derives the connection port per environment at runtime', () => {
    // Port policy lives in the generated Supabase layer, not the Database BB or
    // deploy scripts. One stored string; Lambda → transaction pooler (6543) with
    // prepared statements disabled, local → session port (5432).
    assert.ok(output.includes('async function resolveConnString()'));
    assert.ok(output.includes('process.env.BLOCKS_STACK_NAME'));
    assert.ok(output.includes("u.port = '6543'"));
    assert.ok(output.includes("u.port = '5432'"));
    assert.ok(output.includes("u.searchParams.set('prepared_statements', 'false')"));
    // Database consumes the resolver via the { get } shape, not the AppSetting directly.
    assert.ok(output.includes('fromExisting({ connectionString: { get: resolveConnString }, ssl: resolveDbSsl() })'));
    // A non-URL-parseable string (e.g. unencoded password) must not crash the
    // runtime — fall back to the raw string and let pg parse it.
    assert.ok(output.includes('catch'));
    assert.ok(output.includes('return raw;'));
  });

  test('emits rlsPolicy when tables have RLS', () => {
    assert.ok(output.includes("rlsPolicy: 'enforce'"));
  });

  test('generated wiring verifies TLS by default via CA pinning (visible, editable)', () => {
    // The connection passes an explicit ssl config (resolveDbSsl), so the secure
    // default is visible in the generated file, not hidden in an engine default.
    assert.ok(output.includes('function resolveDbSsl()'));
    assert.ok(output.includes('ssl: resolveDbSsl()'));
    // Primary path: the project CA captured at pull time, committed in database.ca.ts
    // and bundled into the function (works in Lambda — no runtime file/env needed).
    assert.ok(output.includes("import { DATABASE_CA_CERT } from './database.ca.js'"));
    assert.ok(output.includes('DATABASE_CA_CERT'));
    assert.ok(output.includes('rejectUnauthorized: true'));
    // DATABASE_CA_CERT env override (inline PEM or file path).
    assert.ok(output.includes('process.env.DATABASE_CA_CERT'));
    assert.ok(output.includes('readFileSync'));
    // Visible, commented opt-out fallback when no CA is available.
    assert.ok(output.includes('rejectUnauthorized: false'));
    assert.ok(output.includes('prod-ca-2021'));
    // In the deployed function a missing CA must fail closed, not connect unverified.
    assert.ok(output.includes('AWS_LAMBDA_FUNCTION_NAME'));
    assert.ok(output.includes('refusing to connect'));
    // Logs the resolved TLS mode so success isn't silent.
    assert.ok(output.includes('DB TLS: will verify'));
    assert.ok(output.includes('DB TLS: server certificate NOT verified'));
  });

  test('resolveConnString strips sslmode so the pinned CA takes effect', () => {
    assert.ok(output.includes("u.searchParams.delete('sslmode')"));
  });

  test('wires CRUD from tableMeta (data-driven, not a hardcoded list)', () => {
    // Table list is derived from tableMeta so re-pull table changes are picked
    // up without regenerating supabase.ts. The cast preserves per-table types.
    assert.ok(output.includes('tables: Object.keys(tableMeta) as (keyof typeof tableMeta)[]'));
    // No hardcoded table-name literals in the wiring.
    assert.ok(!output.includes("tables: ['migration_test_todos'"));
  });

  test('header states the file is preserved on re-pull (not regenerated)', () => {
    assert.ok(output.includes('re-pull will not overwrite this file'));
    assert.ok(!output.includes('safe to regenerate'));
  });
});

