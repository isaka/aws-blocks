// ── Re-pull preserves hand-edited singulars ────────────────────────────

describe('re-pull preserves hand-edited singulars', () => {
  test('parseExistingMetaSingulars extracts singular values', () => {
    const existing = `// Generated by \`db pull\` — safe to regenerate
import type { Data, Users } from './database.types.js';

export const tableMeta = {
  data: {
    singular: 'dataset',
    plural: 'data',
    primaryKey: 'id',
    columns: ['id', 'value'],
    autoGenerated: ['id'],
  },
  users: {
    singular: 'user',
    plural: 'users',
    primaryKey: 'id',
    columns: ['id', 'name'],
    autoGenerated: ['id'],
  },
} as const;
`;
    const singulars = parseExistingMetaSingulars(existing);
    assert.strictEqual(singulars.get('data'), 'dataset');
    assert.strictEqual(singulars.get('users'), 'user');
  });

  test('ignores a commented-out singular note left below the active value', () => {
    const existing = `export const tableMeta = {
  data: {
    singular: 'dataset',
    // singular: 'datum',  original
    plural: 'data',
  },
} as const;`;
    assert.strictEqual(parseExistingMetaSingulars(existing).get('data'), 'dataset');
  });

  test('ignores a commented-out singular note left above the active value', () => {
    const existing = `export const tableMeta = {
  data: {
    // singular: 'datum',  original
    singular: 'dataset',
    plural: 'data',
  },
} as const;`;
    assert.strictEqual(parseExistingMetaSingulars(existing).get('data'), 'dataset');
  });

  test('returns an empty map for an empty or content-free file', () => {
    assert.strictEqual(parseExistingMetaSingulars('').size, 0);
    assert.strictEqual(parseExistingMetaSingulars('   \n\n\t').size, 0);
    // A header-only / no-table file (e.g. zero eligible tables) also yields nothing.
    assert.strictEqual(parseExistingMetaSingulars('// Generated by `db pull`\nexport const tableMeta = {} as const;\n').size, 0);
  });

  test('ignores a whole table entry commented out with a block comment', () => {
    const existing = `export const tableMeta = {
  /* data: {
    singular: 'datum',
  }, */
  users: {
    singular: 'member',
    plural: 'users',
  },
} as const;`;
    const singulars = parseExistingMetaSingulars(existing);
    assert.strictEqual(singulars.get('users'), 'member');
    assert.strictEqual(singulars.has('data'), false);
  });

  test('generateMetaFile preserves hand-edited singular on re-pull', () => {
    // Simulate: user edited 'data' table singular from 'datum' to 'dataset'
    const tables: TableInfo[] = [{
      name: 'data',
      columns: [
        { table_name: 'data', column_name: 'id', data_type: 'uuid', is_nullable: 'NO', column_default: 'gen_random_uuid()', ordinal_position: 1 },
        { table_name: 'data', column_name: 'value', data_type: 'text', is_nullable: 'NO', column_default: null, ordinal_position: 2 },
      ],
      primaryKey: 'id',
      autoGenerated: ['id'],
      hasRls: false,
      missingGrants: false,
    }];

    // Without existing singulars — pluralize generates 'datum'
    const freshOutput = generateMetaFile(tables);
    assert.ok(freshOutput.includes("singular: 'datum'"));

    // With existing singulars — hand-edited 'dataset' is preserved
    const existingSingulars = new Map([['data', 'dataset']]);
    const repullOutput = generateMetaFile(tables, existingSingulars);
    assert.ok(repullOutput.includes("singular: 'dataset'"));
    assert.ok(!repullOutput.includes("singular: 'datum'"));
  });

  test('new tables get fresh singulars even when existing map is provided', () => {
    const tables: TableInfo[] = [
      {
        name: 'data',
        columns: [{ table_name: 'data', column_name: 'id', data_type: 'uuid', is_nullable: 'NO', column_default: null, ordinal_position: 1 }],
        primaryKey: 'id', autoGenerated: [], hasRls: false, missingGrants: false,
      },
      {
        name: 'tasks',
        columns: [{ table_name: 'tasks', column_name: 'id', data_type: 'uuid', is_nullable: 'NO', column_default: null, ordinal_position: 1 }],
        primaryKey: 'id', autoGenerated: [], hasRls: false, missingGrants: false,
      },
    ];

    // Only 'data' has an existing override; 'tasks' is new
    const existingSingulars = new Map([['data', 'dataset']]);
    const output = generateMetaFile(tables, existingSingulars);
    assert.ok(output.includes("singular: 'dataset'")); // preserved
    assert.ok(output.includes("singular: 'task'"));    // freshly derived
  });

  // Regression for the double-`singular` footgun: generated meta declares
  // `singular` twice per table (runtime const + type mirror). The parser must
  // preserve the CONST value even when only the const was hand-edited.
  const DATA_TABLE: TableInfo[] = [{
    name: 'data',
    columns: [{ table_name: 'data', column_name: 'id', data_type: 'uuid', is_nullable: 'NO', column_default: null, ordinal_position: 1 }],
    primaryKey: 'id', autoGenerated: [], hasRls: false, missingGrants: false,
  }];

  test('parser preserves const edit when only the const block was edited', () => {
    const fresh = generateMetaFile(DATA_TABLE); // both occurrences are 'datum'
    assert.ok(fresh.includes("singular: 'datum'"));
    // Edit ONLY the first occurrence (the runtime tableMeta const)
    const editedConstOnly = fresh.replace("singular: 'datum'", "singular: 'dataset'");
    const parsed = parseExistingMetaSingulars(editedConstOnly);
    assert.strictEqual(parsed.get('data'), 'dataset');
  });

  test('full round-trip through real generator output preserves the edit', () => {
    // 1. fresh generation
    const fresh = generateMetaFile(DATA_TABLE);
    // 2. user edits the singular (both occurrences)
    const edited = fresh.replaceAll("singular: 'datum'", "singular: 'dataset'");
    // 3. parse the edited file
    const parsed = parseExistingMetaSingulars(edited);
    assert.strictEqual(parsed.get('data'), 'dataset');
    // 4. regenerate with preservation — edit survives in BOTH const and type mirror
    const regen = generateMetaFile(DATA_TABLE, parsed);
    assert.ok(regen.includes("singular: 'dataset'"));
    assert.ok(!regen.includes("singular: 'datum'"));
    // and re-parsing the regenerated file is stable
    assert.strictEqual(parseExistingMetaSingulars(regen).get('data'), 'dataset');
  });
});

